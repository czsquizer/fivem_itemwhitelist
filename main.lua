AddEventHandler('entityCreating', function(id)    
    local model = GetEntityModel(id)
    local eType = GetEntityType(id)
    if eType == CONST_ENTITY_TYPE_OBJECT then --Objects
        if not SConfig.WhitelistedObjects[model] then
            if model ~= 0 then
                print(string.format('Model not whitelisted %s, entity owner ID: %s', model, NetworkGetEntityOwner(id)))
                DiscordWebhook("model", model, NetworkGetEntityOwner(id), id)

            end
            CancelEvent()
        end
    elseif eType == CONST_ENTITY_TYPE_VEHICLE then
        for i, v in pairs(SConfig.BlacklistedVehicles) do
            local v = (type(v) == 'number' and v or GetHashKey(v))
            if v == model then
                print(string.format('Vehicle not whitelisted %s, entity owner ID: %s', model, NetworkGetEntityOwner(id)))
                DiscordWebhook("car", model, NetworkGetEntityOwner(id), id)
                CancelEvent()
            end
        end
        local speed = GetEntityVelocity(id)
        if #(speed-vector3(0,0,0)) > 35.0 then
            CancelEvent()
        end
    end
end)

